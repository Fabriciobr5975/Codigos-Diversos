    private void itmAlterarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itmAlterarProdutoActionPerformed
        //0 - Variáveis que pega o nome do produto
        String nome;

        nome = JOptionPane.showInputDialog(null, "Produto a alterar", "Alterar o Produto", JOptionPane.INFORMATION_MESSAGE);

        if (nome == null) {
            return;
        }

        try {
            ResultSet produto = new TesteDao().buscarProdutos(nome);

            if (produto.next()) { //Se encontrar o produto
                String id, name, marca, composicao;

                //Adiciona as variáveis criadas acima e adiciona com cada atributo(coluna) no BD;
                id = produto.getString("id");
                name = produto.getString("nome");
                marca = produto.getString("marca");
                composicao = produto.getString("composicao");

                //Liga a tela/classe para alterar os produtos;
                new AlterarProdutos(id, name, marca, composicao).setVisible(true);

            } else {
                JOptionPane.showMessageDialog(null, "Produto não encontrado", "Produto", JOptionPane.WARNING_MESSAGE);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: " + ex.getMessage());
        }

    }//GEN-LAST:event_itmAlterarProdutoActionPerformed

