    private void itmExcluirProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itmExcluirProdutoActionPerformed
        // Pegar o id do produto;
        String idProduto;

        idProduto = JOptionPane.showInputDialog(null, "Digite o código do produto para excluí-lo", "Excluir produto", JOptionPane.INFORMATION_MESSAGE);

        if (idProduto == null) {
            return;
        }

        try {
            int retorno = new TesteDao().excluirProduto(idProduto);

            if (retorno == 1) { // Se o retorno da variável int "retorno" for 1, ou seja se o produto for encontrado; 
                JOptionPane.showMessageDialog(null, "Produto excluido com sucesso", "Produto Excluido", JOptionPane.INFORMATION_MESSAGE);

            } else { // Se não encontrar o produto
                JOptionPane.showMessageDialog(null, "Produto não encontrado", "Mensagem", JOptionPane.WARNING_MESSAGE);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: " + ex.getMessage());

        }
    }//GEN-LAST:event_itmExcluirProdutoActionPerformed

    private void itmBuscarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itmBuscarProdutoActionPerformed
        //0 - Variável para buscar o produto pelo nome;
        String nome;

        nome = JOptionPane.showInputDialog(null, "Produto a buscar", "Buscar Produto", JOptionPane.INFORMATION_MESSAGE);

        if (nome == null) {
            return;
        }

        try {
            ResultSet produto = new TesteDao().buscarProdutos(nome);

            if (produto.next()) { //Verifica se o produto foi encontrado
                String id, name, marca, composicao;

                //As variáveis criadas acima vão pegar os dados de cada coluna do BD;
                id = produto.getString("id");
                name = produto.getString("nome");
                marca = produto.getString("marca");
                composicao = produto.getString("composicao");

                // Imprimi na tela os valores que as variáveis puxaram do BD puxou;
                JOptionPane.showMessageDialog(null,
                        "ID do Produto: " + id + "\n"
                        + "Nome: " + name + "\n"
                        + "Marca: " + marca + "\n"
                        + "Composição: " + composicao
                );

            } else { // Se o produto não for encotrado retorna essa mensagem de erro
                JOptionPane.showMessageDialog(null, "Produto não encontrado", "Produto", JOptionPane.WARNING_MESSAGE);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_itmBuscarProdutoActionPerformed

